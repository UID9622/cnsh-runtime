        金值 = 结果["五行數據"]["金"]
        if 金值 < 0.1:
            self.熔断触发次数 += 1
            日志.warning(f"🔴 風控熔斷! 金值={金值:.4f} < 0.1")
            return "🔴 風控熔斷 - 資金安全閾值觸發"

        return "🟢 正常"

