    def 五行看板(self) -> str:
        """
        Five Elements Dashboard - 输出当前五行状态看板
        """
        看板 = []
        看板.append("\\n" + "═"*52)
        看板.append("║              🌿 五行狀態看板 🌿                   ║")
        看板.append("═"*52)

        if not self.五行引擎.历史决策:
            看板.append("║  尚無五行決策數據                                   ║")
        else:
            最新 = self.五行引擎.历史决策[-1]
            五行數據 = 最新["五行數據"]

            元素圖標 = {"金": "⚱️", "木": "🌳", "水": "💧", "火": "🔥", "土": "🏔️"}

            for 元素, 值 in 五行數據.items():
                圖標 = 元素圖標[元素]
                條長度 = int(值 * 30)
                條 = "█" * 條長度 + "░" * (30 - 條長度)
                看板.append(f"║  {圖標} {元素}: [{條}] {值:.2f}           ║")

            看板.append("─"*52)
            看板.append(f"║  ⚖️ 平衡指數: {最新['平衡指数']:.4f}                          ║")
            看板.append(f"║  🔄 生克強度: {最新['生克强度']:.4f}                          ║")
            看板.append(f"║  ☯️ 三才系數: {最新['三才系数']:.4f}                          ║")
            看板.append(f"║  📊 複合決策: {最新['复合决策强度']:.4f}                        ║")
            看板.append(f"║  💡 決策建議: {最新['決策建議'][:26]:26}    ║")
            看板.append(f"║  🔒 熔斷狀態: {最新['熔斷狀態'][:26]:26}    ║")

        看板.append("═"*52)
        结果 = "\\n".join(看板)
        print(结果)
        return 结果

    def 卦象显示(self) -> str:
        """
        Hexagram Display - 输出当前卦象
        """
        显示 = []
        显示.append("\\n" + "═"*52)
        显示.append("║              🔮 卦象顯示 🔮                       ║")
        显示.append("═"*52)

        if not self.卦审计器.卦象历史:
            显示.append("║  尚無卦象數據                                       ║")
        else:
            最新 = self.卦审计器.卦象历史[-1]

            显示.append(f"║  卦名: {最新.卦名:36}     ║")
            显示.append(f"║  卦象: {最新.卦象:36}     ║")
            显示.append(f"║  卦序: 第{最新.卦序号}卦{'':29}     ║")
            显示.append(f"║  動爻: {str(最新.动爻列表):36}     ║")
            显示.append(f"║  吉凶: {最新.吉凶判定:36}     ║")
            显示.append("─"*52)

            显示.append("║  📐 八維度評分:                                   ║")
            for 维度, 分 in 最新.八维度评分.items():
                星級 = "★" * int(分 * 5) + "☆" * (5 - int(分 * 5))
                显示.append(f"║     {维度}: {分:.2f} {星級:11}            ║")

            if 最新.爻辞解读:
                显示.append("─"*52)
                显示.append("║  📖 爻辭解讀:                                     ║")
                for 解读 in 最新.爻辞解读:
                    # 确保不超出边界
                    行 = f"║     {解读[:40]:40}     ║"
                    显示.append(行)

        显示.append("═"*52)
        结果 = "\\n".join(显示)
        print(结果)
        return 结果

